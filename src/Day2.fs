type HandValue =
    | Unknown = -1
    | Rock = 0
    | Paper = 1
    | Scissors = 2

type Outcome =
    | Unknown = -1
    | Lose = 0
    | Win = 1
    | Draw = 2

type Hand =
    abstract member Value: HandValue
    abstract member Worth: int
    abstract member Verse: HandValue -> Outcome

type Rock() =
    interface Hand with
        member this.Value = HandValue.Rock;
        member this.Worth = 1;
        member this.Verse(x: HandValue) = 
            match x with
            | HandValue.Rock -> Outcome.Draw
            | HandValue.Paper -> Outcome.Lose
            | HandValue.Scissors -> Outcome.Win
            | _ -> Outcome.Unknown

type Paper() =
    interface Hand with
        member this.Value = HandValue.Paper;
        member this.Worth = 2;
        member this.Verse(x: HandValue) = 
            match x with
            | HandValue.Rock -> Outcome.Win
            | HandValue.Paper -> Outcome.Draw
            | HandValue.Scissors -> Outcome.Lose
            | _ -> Outcome.Unknown

type Scissors() =
    interface Hand with
        member this.Value = HandValue.Scissors;
        member this.Worth = 3;
        member this.Verse(x: HandValue) = 
            match x with
            | HandValue.Rock -> Outcome.Lose
            | HandValue.Paper -> Outcome.Win
            | HandValue.Scissors -> Outcome.Draw
            | _ -> Outcome.Unknown

type Rules() =
    member this.Hands : list<Hand> =
        [new Rock(); new Scissors(); new Paper()]

    member this.Verse (player: HandValue, enemy: HandValue) : int =
        let playerHand = this.Hands |> List.find (fun x -> x.Value.Equals player)
        match playerHand.Verse(enemy) with
        | Outcome.Win -> playerHand.Worth + 6
        | Outcome.Draw -> playerHand.Worth + 3
        | Outcome.Lose -> playerHand.Worth + 0
        | _ -> 0

    member this.VerseSneaky (player: HandValue, enemy: HandValue) : int =
        let outcome = 
            match player with
            | HandValue.Rock -> Outcome.Lose
            | HandValue.Paper -> Outcome.Draw
            | HandValue.Scissors -> Outcome.Win
            | _ -> Outcome.Unknown

        let newPlayerHand = this.Hands |> List.find (fun x -> (x.Verse enemy).Equals outcome)
        match newPlayerHand.Verse(enemy) with
        | Outcome.Win -> newPlayerHand.Worth + 6
        | Outcome.Draw -> newPlayerHand.Worth + 3
        | Outcome.Lose -> newPlayerHand.Worth + 0
        | _ -> 0

let rules = new Rules()

let parseEnemyChoice x =
    match x with
    | "A" -> HandValue.Rock
    | "B" -> HandValue.Paper
    | "C" -> HandValue.Scissors
    | _ -> HandValue.Unknown

let parsePlayerChoice x =
    match x with
    | "X" -> HandValue.Rock
    | "Y" -> HandValue.Paper
    | "Z" -> HandValue.Scissors
    | _ -> HandValue.Unknown

let parseString (x: string) =
    let inputs = x.Split(' ')
    let enemyInput = inputs[0] |> parseEnemyChoice
    let playerInput = inputs[1] |> parsePlayerChoice
    playerInput, enemyInput

